package com.herokuapp.PrivateLearningApp.model.request;

public class TeacherUpdateRequest {
    private String name, phone, address, gender, birth_date, education, gpa, latitude, longitude;

    public TeacherUpdateRequest(String name, String phone, String address, String gender, String birth_date, String education, String gpa, String latitude, String longitude) {
        this.name = name;
        this.phone = phone;
        this.address = address;
        this.gender = gender;
        this.birth_date = birth_date;
        this.education = education;
        this.gpa = gpa;
        this.latitude = latitude;
        this.longitude = longitude;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getBirth_date() {
        return birth_date;
    }

    public void setBirth_date(String birth_date) {
        this.birth_date = birth_date;
    }

    public String getEducation() {
        return education;
    }

    public void setEducation(String education) {
        this.education = education;
    }

    public String getGpa() {
        return gpa;
    }

    public void setGpa(String gpa) {
        this.gpa = gpa;
    }

    public String getLatitude() {
        return latitude;
    }

    public void setLatitude(String latitude) {
        this.latitude = latitude;
    }

    public String getLongitude() {
        return longitude;
    }

    public void setLongitude(String longitude) {
        this.longitude = longitude;
    }
}
